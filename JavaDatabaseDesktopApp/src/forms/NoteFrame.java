    private void backButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backButtonActionPerformed
        // TODO add your handling code here:
        if(state == State.scroll){
            position--;
            setNote();
            checkPosition();
        }
        else if(state == State.modify){
            state = State.scroll;
            setView();
        }
        else if(state == State.addNew){
            state = State.scroll;
            setView();
            setNote();
        }

    }//GEN-LAST:event_backButtonActionPerformed

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        state = State.modify;
        setView();
    }//GEN-LAST:event_updateButtonActionPerformed

    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        if(state == State.modify){
            db.updateNote(id, titleTextField.getText(), noteTextArea.getText(), (int)importanceSpinner.getValue());
            state = State.scroll;
            setView();
        }
        else if(state == State.addNew){
            db.addNote(titleTextField.getText(), noteTextArea.getText(), (int)importanceSpinner.getValue());
            state = State.scroll;
            notes = db.getNotes();
            position = notes.size()-1;
            setView();
            setNote();
        }
    }//GEN-LAST:event_saveButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        db.removeNote(id);
        notes = db.getNotes();
    }//GEN-LAST:event_deleteButtonActionPerformed

