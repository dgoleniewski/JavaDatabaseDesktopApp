    public void setNote(){
        Note note = (Note)notes.get(position);
        id = note.getId();
        idLabel.setText("ID: " + id);
        dateLabel.setText("Creation date: " + note.getCreationdate().toString());
        titleTextField.setText(note.getTitle());
        noteTextArea.setText(note.getNote());
        importanceSpinner.setValue(note.getImportance());
    }
    
    public void setView(){
        switch(state){
            case scroll:
                titleTextField.setEnabled(false);
                noteTextArea.setEnabled(false);
                importanceSpinner.setEnabled(false);
                saveButton.setEnabled(false);
                updateButton.setEnabled(true);
                newButton.setEnabled(true);
                deleteButton.setEnabled(true);
                checkPosition();
                break;
            case addNew:
                titleTextField.setEnabled(true);
                noteTextArea.setEnabled(true);
                importanceSpinner.setEnabled(true);
                backButton.setEnabled(true);
                nextButton.setEnabled(false);
                saveButton.setEnabled(true);
                updateButton.setEnabled(false);
                newButton.setEnabled(false);
                deleteButton.setEnabled(false);
                break;
            case modify:
                titleTextField.setEnabled(true);
                noteTextArea.setEnabled(true);
                importanceSpinner.setEnabled(true);
                backButton.setEnabled(true);
                nextButton.setEnabled(false);
                saveButton.setEnabled(true);
                updateButton.setEnabled(false);
                newButton.setEnabled(false);
                deleteButton.setEnabled(false);
                break;
        }
    }
    
    public void checkPosition(){
        if(position == 0 && notes.size()==1){
            backButton.setEnabled(false);
            nextButton.setEnabled(false);
        }
        else if(position == 0){
            backButton.setEnabled(false);
            nextButton.setEnabled(true);
        }
        else if(position == notes.size()-1){
            nextButton.setEnabled(false);
            backButton.setEnabled(true);
        }
        else{
            backButton.setEnabled(true);
            nextButton.setEnabled(true);
        }
    }

